public class CharacterConstants {

    public static final char LEFT_PAREN = '(';

    public static final char RIGHT_PAREN = ')';

    public static boolean isEqualToLeftParen( final char character ) {
        return LEFT_PAREN == character;
    }

    public static boolean isEqualToRightParen( final char character ) {
        return RIGHT_PAREN == character;
    }
}
